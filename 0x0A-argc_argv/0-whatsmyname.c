#include <stdio.h>

/**
 * main - program that prints its name followed by new line
 * @argc: the counts of arguments or number of argument supplied
 * @argv: An array of pointer to the arguments
 * Return: 0
 *
 */
int main(int __attribute((unused)) argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);

}
